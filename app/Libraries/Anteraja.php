        $secret_access_key = env('ANTERAJA_SECRET_ACCESS_KEY');

        // if (!$access_key_id || !$secret_access_key) {
        if (!$access_key_id) {
            return [
                'status' => false,
                'info' => 'Auth credentials for Anteraja is not set'
            ];
        }

        // generate booking_id
        $prefix = env('ANTERAJA_PREFIX_ORDER');
        $booking_id = $prefix . '-' . $invoice_no;

        // if (!in_array($service_type, ['REG', 'ND', 'SD'])) {
        //     switch ($service_type) {
        //         case 'Regular':
        //             $service_type = 'REG';
        //             break;
        //         case 'Anteraja Regular':
        //             $service_type = 'REG';
        //             break;

        //         case 'Next Day':
        //             $service_type = 'ND';
        //             break;
        //         case 'Anteraja Next Day':
        //             $service_type = 'ND';
        //             break;

        //         case 'Same Day':
        //             $service_type = 'SD';
        //             break;
        //         case 'Anteraja Same Day':
        //             $service_type = 'SD';
        //             break;

        //         default:
        //             # ERROR
        //             return [
        //                 'status' => false,
        //                 'info' => 'Invalid Service Type'
        //             ];
        //             break;
        //     }
        // }
        if (empty($service_type)) {
            # ERROR
            return [
                'status' => false,
                'info' => 'Invalid Service Type'
            ];
        }

        if ($total_weight_grams < 1000) {
            $total_weight_grams = 1000;
        } elseif ($total_weight_grams > 50000) {
            # ERROR
            return [
                'status' => false,
                'info' => 'Total Weight exceeded'
            ];
        }

        try {
            $client = new Client();
            // $headers = [
            //     'access-key-id' => $access_key_id,
            //     'secret-access-key' => $secret_access_key,
            //     'Content-Type' => 'application/json',
            // ];
            // $json = [
            //     'booking_id' => $booking_id,
            //     'invoice_no' => $invoice_no,
            //     'service_code' => $service_type,
            //     'parcel_total_weight' => $total_weight_grams,
            //     'shipper' => $shipper,
            //     'receiver' => $receiver,
            //     'items' => $items,
            //     'use_insurance' => $use_insurance,
            //     'declared_value' => $declared_value,
            //     'expect_time' => $expect_time
            // ];
            // $result = $client->request('POST', $endpoint, [
            //     'headers' => $headers,
            //     'json' => $json
            // ]);
            // $response = json_decode($result->getBody()->getContents());
            $headers = [
                'x-api-key' => $access_key_id,
            ];
            
            $payment_method = implode(' ', explode('_', $data->payment_method));
            $shipping_type = explode(' ', $data->shipper_service_type);
            $shipping = strtoupper($shipping_type[0]);
            $brand_name = $data->product_item_name . ' ' . $data->product_item_variant_name;
            
            $json = [
                "order_date" => $data->paid_at,                        
                "brand_name" => $brand_name,                                    
                "shipper_name" => $data->shipper_name,                    
                "shipper_phone" => $shipper->phone,                           
                "shipper_destination_id" => 1,                            
                "shipper_address" => "Jl. Nasional 12, RT.6/RW.2, Grogol Selatan., Kec. Kebayoran Lama, Kota Jakarta Selatan",                  
                "shipper_email" => $shipper->email,                          
                "receiver_name" => $receiver->name,                                 
                "receiver_phone" => $receiver->phone,                          
                "receiver_destination_id" => 39947,                           
                "receiver_address" => $receiver->address,     
                "shipping" => $shipping,                                          
                "shipping_type" => $shipping_type[1],                                      
                "payment_method" => $payment_method,                                    
                "shipping_cost" => $data->price_shipping,
                "shipping_cashback" => 0,
                "service_fee" => 0,
                "additional_cost" => 0,
                "grand_total" => $data->price_total,
                "cod_value" => 0,
                "insurance_value" => 0,
                "order_details" => $items
            ];
            // dd($json, $headers, $endpoint);
            $result = $client->request('POST', $endpoint, [
                'headers' => $headers,
                'json' => $json,
                'debug' => true
            ]);
            $response = json_decode($result->getBody()->getContents());

            // logging
            DB::table('courier_anteraja_logs')->insert(
                [
                    'method' => 'POST',
                    'endpoint' => $endpoint,
                    'headers' => json_encode($headers),
                    'params_type' => 'json',
                    'params' => json_encode($json),
                    'response' => json_encode($response),
                    'created_at' => date('Y-m-d H:i:s'),
                    'updated_at' => date('Y-m-d H:i:s')
                ]
            );

            $headers_pickup = [
                'x-api-key' => $access_key_id,
                'Content-Type' => 'application/json',
                'Accept' => 'application/json'
            ];

            $pickupDateTime = new \DateTime('now', new \DateTimeZone('Asia/Jakarta'));
            $pickupDateTime->modify('+2 hours');

            $json_pickup = [
                "pickup_date" => $pickupDateTime->format('Y-m-d'),
                "pickup_time" => $pickupDateTime->format('H:i'),
                "pickup_vehicle" => "Motor", // Motor/Mobil/Truck
                "orders" => [
                    [
                        "order_no" => $response->data->order_no
                    ]
                ]
            ];
            $result_pickup = $client->request('POST', $endpoint_pickup, [
                'headers' => $headers_pickup,
                'json' => $json_pickup,
                'debug' => true
            ]);
            $response_pickup = json_decode($result_pickup->getBody()->getContents());

            return [
                'status' => true,
                'data' => $response_pickup->data
            ];
        } catch (\GuzzleHttp\Exception\ClientException $e) {
            $response = $e->getResponse();
            // dd('asd',$response, $response->getBody()->getContents(), $e);
            $responseBodyAsString = json_decode($response->getBody()->getContents());
            // dd($responseBodyAsString->meta->message);
            # ERROR
            return [
                'status' => false,
                'info' => $responseBodyAsString->meta->message
            ];

            // * Error Response Sample
            // array:2 [▼
            //     "status" => false
            //     "info" => "District not found : 31.72.55"
            // ]
        }

        # INTERNAL ERROR
        return [
            'status' => false,
            'info' => 'Internal Server Error in Anteraja Library'
        ];
    }

