    {
        // $endpoint = env('ANTERAJA_BASE_PATH') . '/order';
        $endpoint = env('RAJAONGKIR_BASE_PATH') . '/order/api/v1/orders/store';
        $endpoint_pickup = env('RAJAONGKIR_BASE_PATH') . '/order/api/v1/pickup/request';

